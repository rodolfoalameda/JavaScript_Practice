var num1 = 3;
let num2 = 4;

console.log(num1, num2);

/* Declarar variaveis mais com let
Com var, você pode redeclarar uma mesma variavel
Com let, não */

num1 = 12;
num2 = 25;

console.log(num1, num2);

const num3 = 9999;

//Valor constante, não pode ser redeclarado.

console.log(num3);

/* Revisão: Palavras reservadas para declaracao:
var, let e const. Usar mais o let.
const e let são variaveis que não podem ser redeclaradas */